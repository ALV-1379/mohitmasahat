using System;
using System.Collections.Generic;
using System.Text;

namespace mohitmasahat
{
    class square
    {
        private double a;

        public square(double s)
        {
            a = s;
        }

        public double masahat()
        {
            return a * a;
        }

        public double mohit()
        {
            return 4 * a;
        }
    }
}
